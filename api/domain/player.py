class Player:
    def __init__(self,
                 name: str,
                 date_of_birth: str,
                 nationality: str,
                 position: str,
                 shirt_number: int,
                 code: str):
        self.name = name
        self.date_of_birth = date_of_birth
        self.nationality = nationality
        self.position = position
        self.shirt_number = shirt_number
        self.code = code
